from sklearn.cluster import DBSCAN
from sklearn.mixture import GaussianMixture
from sklearn.neighbors import NearestNeighbors
from sklearn.metrics import (silhouette_score, davies_bouldin_score,
                             adjusted_rand_score)
X_train_pca = np.load('X_train_pca_checkpoint.npy')
y_train      = np.load('y_train_checkpoint.npy')

from sklearn.decomposition import PCA
X_train_scaled = np.load('X_train_scaled_checkpoint.npy')
Z_pca = PCA(n_components=2, random_state=42).fit_transform(X_train_scaled)

labels_fauna = ['Especie 10', 'Especie 12', 'Especie 17', 'Especie 18', 'Especie 23']
print(f"Espacio de clustering: {X_train_pca.shape}  |  Espacio de gráfico: {Z_pca.shape}")

min_samples = 2 * X_train_pca.shape[1]
print(f"min_samples = {min_samples} (heurística 2*dim)")

nn = NearestNeighbors(n_neighbors=min_samples).fit(X_train_pca)
distances, _ = nn.kneighbors(X_train_pca)
k_dist = np.sort(distances[:, -1])

x = np.arange(len(k_dist))
x1, y1, x2, y2 = x[0], k_dist[0], x[-1], k_dist[-1]
numer = np.abs((y2 - y1) * x - (x2 - x1) * k_dist + x2 * y1 - y2 * x1)
denom = np.hypot(y2 - y1, x2 - x1)
eps_idx = np.argmax(numer / denom)
eps_est = k_dist[eps_idx]
print(f" eps estimado en el codo = {eps_est:.3f}")

plt.figure(figsize=(7.5, 4.5))
plt.plot(k_dist, color='#003087', linewidth=2)
plt.axhline(eps_est, color='red', linestyle='--',
            label=f'eps (codo) = {eps_est:.2f}')
plt.axvline(eps_idx, color='gray', linestyle=':', alpha=0.7)
plt.title(f"k-distance plot (k = {min_samples})")
plt.xlabel("Puntos ordenados por distancia")
plt.ylabel(f"Distancia al {min_samples}º vecino")
plt.legend(loc='upper left')
plt.grid(True, linestyle='--', alpha=0.5)
plt.tight_layout()
plt.show()

print("Barrido de eps alrededor del valor estimado:\n")
barrido_db = []
for factor in [0.5, 0.6, 0.7, 0.8, 0.9, 1.0]:
    eps = eps_est * factor
    lab = DBSCAN(eps=eps, min_samples=min_samples).fit(X_train_pca).labels_
    n_cl = len(set(lab)) - (1 if -1 in lab else 0)
    n_noise = int((lab == -1).sum())
    barrido_db.append({'eps': round(eps, 2), 'clusters': n_cl, 'ruido': n_noise, 'ruido_%': round(100*n_noise/len(lab), 1)})
print(pd.DataFrame(barrido_db).to_string(index=False))

dbscan = DBSCAN(eps=eps_est, min_samples=min_samples).fit(X_train_pca)
labels_dbscan = dbscan.labels_
n_clusters_db = len(set(labels_dbscan)) - (1 if -1 in labels_dbscan else 0)
n_noise_db = int((labels_dbscan == -1).sum())
print(f"\n DBSCAN final -> clusters = {n_clusters_db} | " f"ruido = {n_noise_db} ({100*n_noise_db/len(labels_dbscan):.1f}%)")

# gaussian mixture model (paradigma probabilístico)
# n_components mediante BIC y AIC
n_range = range(2, 9)
bics, aics = [], []
print("Barrido de componentes para GMM (BIC / AIC):\n")
for n in n_range:
    gmm_tmp = GaussianMixture(n_components=n, covariance_type='full',
                              random_state=42, n_init=2).fit(X_train_pca)
    bics.append(gmm_tmp.bic(X_train_pca))
    aics.append(gmm_tmp.aic(X_train_pca))
    print(f"    n={n}:  BIC={bics[-1]:.0f}   AIC={aics[-1]:.0f}")

best_n = list(n_range)[int(np.argmin(bics))]
print(f"\nn_components óptimo (mínimo BIC) = {best_n}")

plt.figure(figsize=(7.5, 4.5))
plt.plot(list(n_range), bics, 'o-', label='BIC', color='#003087', linewidth=2)
plt.plot(list(n_range), aics, 's--', label='AIC', color='#C1272D', linewidth=2)
plt.axvline(best_n, color='gray', linestyle=':', label=f'Óptimo (n={best_n})')
plt.title("Selección de componentes GMM — BIC vs AIC")
plt.xlabel("Número de componentes")
plt.ylabel("Criterio de información")
plt.legend(loc='best')
plt.grid(True, linestyle='--', alpha=0.5)
plt.tight_layout()
plt.show()

gmm = GaussianMixture(n_components=best_n, covariance_type='full', # ajuste final del gmm con el número óptimo de componentes
                      random_state=42, n_init=2).fit(X_train_pca)
labels_gmm = gmm.predict(X_train_pca)
print(f"GMM ajustado con {best_n} componentes ")
print(f"    Distribución de puntos por componente:")
print(pd.Series(labels_gmm).value_counts().sort_index().to_string())

# validación intenrea
def metricas_internas(X, labels):
    mask = labels != -1
    etiquetas_validas = set(labels[mask])
    if len(etiquetas_validas) < 2:
        return float('nan'), float('nan')
    sil = silhouette_score(X[mask], labels[mask])
    db = davies_bouldin_score(X[mask], labels[mask])
    return sil, db

sil_db, dbi_db = metricas_internas(X_train_pca, labels_dbscan)
