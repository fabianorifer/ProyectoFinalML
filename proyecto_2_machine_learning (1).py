class MLP(nn.Module):
    def __init__(self, dropout=0.0, batchnorm=False):
        super().__init__()

        layers = []

        layers.append(nn.Linear(input_dim, 256))
        if batchnorm:
            layers.append(nn.BatchNorm1d(256))
        layers.append(nn.ReLU())
        if dropout > 0:
            layers.append(nn.Dropout(dropout))

        layers.append(nn.Linear(256, 128))
        if batchnorm:
            layers.append(nn.BatchNorm1d(128))
        layers.append(nn.ReLU())
        if dropout > 0:
            layers.append(nn.Dropout(dropout))

        layers.append(nn.Linear(128, 64))
        layers.append(nn.ReLU())

        layers.append(nn.Linear(64, num_classes))

        self.model = nn.Sequential(*layers)

    def forward(self, x):
        return self.model(x)


def train_model(model, epochs=25, lr=0.001):

    model = model.to(device)

    criterion = nn.CrossEntropyLoss()
    optimizer = optim.Adam(model.parameters(), lr=lr)

    history = {
        "loss": [],
        "val_loss": [],
        "acc": [],
        "val_acc": []
    }

    for epoch in range(epochs):

        model.train()

        train_loss = 0
        correct = 0
        total = 0

        for X, y in train_loader:

            X, y = X.to(device), y.to(device)

            optimizer.zero_grad()

            outputs = model(X)

            loss = criterion(outputs, y)

            loss.backward()

            optimizer.step()

            train_loss += loss.item()

            preds = torch.argmax(outputs, 1)

            correct += (preds == y).sum().item()

            total += y.size(0)

        train_acc = correct / total
        train_loss = train_loss / len(train_loader)

        model.eval()

        val_loss = 0
        val_correct = 0
        val_total = 0

        with torch.no_grad():

            for i in range(len(X_val)):

                x = X_val[i].unsqueeze(0).to(device)
                y = y_val[i].unsqueeze(0).to(device)

                out = model(x)

                loss = criterion(out, y)

                val_loss += loss.item()

                pred = torch.argmax(out, 1)

                val_correct += (pred == y).sum().item()

                val_total += 1

        val_acc = val_correct / val_total
        val_loss = val_loss / len(X_val)

        history["loss"].append(train_loss)
        history["val_loss"].append(val_loss)
        history["acc"].append(train_acc)
        history["val_acc"].append(val_acc)

        print(f"Epoch {epoch+1}/{epochs} | Loss {train_loss:.4f} | Acc {train_acc:.4f} | Val Loss {val_loss:.4f} | Val Acc {val_acc:.4f}")

    return history


