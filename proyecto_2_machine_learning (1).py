def zona_riesgo(p):
    if p >= 0.85:
        return 'Confianza (>=85%)'
    elif p >= 0.40:
        return 'Incertidumbre (40-85%)'
    else:
        return 'Rechazo (<40%)'

orden_zonas = ['Confianza (>=85%)', 'Incertidumbre (40-85%)', 'Rechazo (<40%)']

def analizar_zonas(y_proba, y_pred, etiqueta):
    p_max = y_proba.max(axis=1)
    zonas = np.array([zona_riesgo(p) for p in p_max])
    conteo = pd.Series(zonas).value_counts().reindex(orden_zonas).fillna(0).astype(int)
    print(f"\n Política de zonas usando probabilidades de: {etiqueta}")
    print(f"    Probabilidad máxima observada en todo el test: {p_max.max():.3f}")
    print(conteo.to_string())
    print("    Porcentaje del test en cada zona:")
    print((100 * conteo / len(zonas)).round(1).to_string())
    print("    Validación empírica (exactitud real dentro de cada zona):")
    for z in orden_zonas:
        mask = zonas == z
        if mask.sum() > 0:
            acc_z = accuracy_score(y_test[mask], y_pred[mask])
            print(f"      [{z}]: {acc_z:.3f}  (n={mask.sum()})")
        else:
            print(f"      [{z}]: zona vacía (n=0)")
    return conteo, p_max

